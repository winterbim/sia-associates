def replace_word(match: re.Match[str]) -> str:
    word = match.group(0)
    repl = REPLACEMENTS[word.lower()]
    if word[0].isupper():
        return repl[0].upper() + repl[1:]
    return repl


def build_regex() -> re.Pattern[str]:
    keys = sorted(REPLACEMENTS.keys(), key=len, reverse=True)
    pattern = r"(?<![A-Za-zÀ-ÿ])(" + "|".join(re.escape(k) for k in keys) + r")(?![A-Za-zÀ-ÿ])"
    return re.compile(pattern, re.IGNORECASE)


def fix_text(text: str, rx: re.Pattern[str]) -> tuple[str, int]:
    new_text, count = rx.subn(replace_word, text)
    return new_text, count


