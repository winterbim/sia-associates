def fix_blog_post(path: Path, rx: re.Pattern[str]) -> int:
    raw = path.read_text(encoding="utf-8")
    data = json.loads(raw)
    total = 0

    def transform(text: str) -> tuple[str, int]:
        new_text, count = fix_text(text, rx)
        for pattern, repl in PHRASE_FIXES:
            new_text, n = re.subn(pattern, repl, new_text)
            count += n
        return new_text, count

    for key in ("title", "excerpt", "content"):
        if key in data and isinstance(data[key], str):
            data[key], n = transform(data[key])
            total += n
    if "tags" in data and isinstance(data["tags"], list):
        for i, tag in enumerate(data["tags"]):
            if isinstance(tag, str):
                data["tags"][i], n = transform(tag)
                total += n
    if total > 0:
        path.write_text(
            json.dumps(data, ensure_ascii=False, indent=2) + "\n",
            encoding="utf-8",
        )
    return total


